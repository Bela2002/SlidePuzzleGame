    private void jButtonEnterNewGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnterNewGameActionPerformed
        // TODO add your handling code here:
        playSoundEffectLogin("Wav/bubble-pop.wav");
        
        String userName = jTextFieldNameBox.getText();
    // Check if the user has entered a name
        if(userName.isEmpty()){
 
            ImageIcon LoginNameIcon = new ImageIcon(getClass().getResource("NameImage.png"));
            JOptionPane.showMessageDialog(this, "<html><b style=\"font-size:10px;\">Please enter a user name.","PUZZLE GAME",JOptionPane.INFORMATION_MESSAGE,LoginNameIcon);
        }
        else{
 
            SlidePuzzleGame gameWindow = new SlidePuzzleGame(userName);

    // Make the game window visible
            gameWindow.setVisible(true);
            gameWindow.setEnabled(true);

    // Close the login window
            this.dispose();
        }
    }//GEN-LAST:event_jButtonEnterNewGameActionPerformed


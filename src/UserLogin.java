    private void playSoundEffectLogin(String soundEffectFilePath) {
       
        try {
           
           InputStream soundEffectFile = SlidePuzzleGame.class.getClassLoader().getResourceAsStream(soundEffectFilePath);
            
            if (soundEffectFile!= null) {
                
                AudioInputStream soundEffectStream = AudioSystem.getAudioInputStream(new BufferedInputStream(soundEffectFile));
                
                Clip soundEffectClip = AudioSystem.getClip();
                soundEffectClip.open(soundEffectStream);
                soundEffectClip.start();
            } 
            else 
            {
                System.out.println("Sound effect file not found: " + soundEffectFilePath);
            }
        } 
        catch (Exception e) {
            
            System.out.println("Error playing sound effect: " + e.getMessage());
        }
    }    
